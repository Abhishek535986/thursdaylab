package String;

public class FirstAndLastOccurence {

	public static void main(String[] args) {
		
		String name = "Hello World";   //input
		
		System.out.println("The first occurence of O is :"+name.indexOf('o'));  //first index
		System.out.println("The last occurence of O is :"+name.lastIndexOf('o'));  //last index

	}

}
